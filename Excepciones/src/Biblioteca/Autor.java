package Biblioteca;

public class Autor {
    String nombre;
    String pseudonimo;

    
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public String getPseudonimo() {
        return pseudonimo;
    }
    public void setPseudonimo(String pseudonimo) {
        this.pseudonimo = pseudonimo;
    }

    
}
